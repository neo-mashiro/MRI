    /// <summary>
    /// Each cube has exactly 8 vertices.
    /// </summary>
    public struct CubeVertex {
        public Vector3 Position;
        public float Value;
        public Vector3 Gradient;
    }

    /// <summary>
    /// The class that holds all configuration of a single cube in the 3D grid.
    /// </summary>
    public class GridCube {
        public CubeVertex[] Vertices { get; } = new CubeVertex[8];
        public int Index { get; set; }

        public List<Vector3> CutVertices { get; }
        public List<Vector3> VertexNormals { get; }

        // offset on the x,y,z axis relative to vertex 0 (see the graph above)
        private static readonly int[][] Offset = {
            new[] {0, 0, 0}, new[] {1, 0, 0}, new[] {1, 0, 1}, new[] {0, 0, 1},
            new[] {0, 1, 0}, new[] {1, 1, 0}, new[] {1, 1, 1}, new[] {0, 1, 1}
        };

        // the 3D volume as a bounding box for all cubes
        public static NiftiImage Volume;

        private (int, int, int) _position;
        private readonly bool _isOnBoundary;

        // constructor
        public GridCube(int i, int j, int k, bool isOnBoundary) {
            _position = (i, j, k);
            _isOnBoundary = isOnBoundary;
            
            Index = 0;
            CutVertices = new List<Vector3>();
            VertexNormals = new List<Vector3>();

            for (var n = 0; n < 8; n++) {
                var v = Offset[n];
                var position = Volume.GetVoxelCoordinate(i + v[0], j + v[1], k + v[2]);

                Vertices[n] = new CubeVertex {
                    Position = 0.1f * new Vector3(position.x, position.y, position.z),
                    Value = Volume[i + v[0], j + v[1], k + v[2]]
                };
            }
        }

        // compute the gradient vector at each one of the 8 vertices
        // we separate this method from the constructor to save computation,
        // so it will only get called on the cubes that actually cut the brain
        public void CalculateGradients() {
            for (var n = 0; n < 8; n++) {
                var v = Offset[n];
                var (i, j, k) = _position;
                var gradient = Vector3.zero;
                
                if (!_isOnBoundary) {
                    gradient = new Vector3(
                        Volume[i + v[0] - 1, j + v[1], k + v[2]] - Volume[i + v[0] + 1, j + v[1], k + v[2]],
                        Volume[i + v[0], j + v[1] - 1, k + v[2]] - Volume[i + v[0], j + v[1] + 1, k + v[2]],
                        Volume[i + v[0], j + v[1], k + v[2] - 1] - Volume[i + v[0], j + v[1], k + v[2] + 1]
                    ).normalized;
                }

                Vertices[n].Gradient = gradient;
            }
        }
    }

    /// <summary>
    /// Implementation of the marching cubes algorithm on a 3D volumetric brain.
    /// </summary>
    public static class MarchingCubes {
        
        /// <summary>
        /// Given a nifti 3D image, calculate the array of vertices and triangle indices of the mesh that
        /// approximates the corresponding brain surface at the given threshold intensity.<br/>
        /// Note: the image must be skull-stripped and denoised first to obtain a precise mesh!
        /// </summary>
        public static void GenerateMesh(NiftiImage brain, float threshold, out Vector3[] vertices,
            out int[] triangles, out Vector3[] normals, bool reverseOrder = false) {
            
            var verticesList = new List<Vector3>();
            var normalsList = new List<Vector3>();

            var nx = brain.Header.dim[1];
            var ny = brain.Header.dim[2];
            var nz = brain.Header.dim[3];
            
            // the way we map the marching cubes grid to the volumetric brain is that, each of the cube's 8
            // vertices corresponds to a brain voxel, so a single cube is at the center of 8 adjacent voxels in 3D.
            // for example, if the brain size is 274x384x384, we'd need 273x383x383 cubes to be marched one by one.

            GridCube.Volume = brain;
            
            for (var i = 0; i < nx - 1; i++) {
                for (var j = 0; j < ny - 1; j++) {
                    for (var k = 0; k < nz - 1; k++) {
                        // check if it's a boundary case
                        var isOnBoundary = i * j * k == 0 || i == nx - 2 || j == ny - 2 || k == nz - 2;

                        // initialize the cube at position [i, j, k]
                        var cube = new GridCube(i, j, k, isOnBoundary);
                        
                        // compute the cube's cut vertices
                        ProcessCube(threshold, ref cube);
                        
                        // append the cut vertices and normals to the list
                        if (cube.CutVertices.Any()) {
                            verticesList.AddRange(cube.CutVertices);
                            normalsList.AddRange(cube.VertexNormals);
                        }
                    }
                }
            }

            vertices = verticesList.ToArray();

            // clockwise winding order
            if (reverseOrder) {
                triangles = Enumerable.Range(0, vertices.Length).Reverse().ToArray();
                normals = normalsList.Select(normal => -normal).ToArray();
            }
            // counter-clockwise winding order (default)
            else {
                triangles = Enumerable.Range(0, vertices.Length).ToArray();
                normals = normalsList.ToArray();
            }
        }

        /// <summary>
        /// Given the isovalue of an isosurface, determine how that surface intersects a specific cube and
        /// updates the cube accordingly. For our brain image, the isosurface corresponds to the grey matter,
        /// so the isovalue should be the threshold value that segments the grey matter from the brain.
        /// </summary>
        private static void ProcessCube(float isoValue, ref GridCube cube) {
            // compute the cube index which we use to query into the lookup table
            for (var i = 0; i < 8; i++) {
                if (cube.Vertices[i].Value < isoValue) {
                    cube.Index |= 1 << i;
                }
            }

            // skip the cube if it is entirely inside or outside the surface
            if (EdgeTable[cube.Index] == 0) {
                return;
            }
            
            // now we know the cube intersects the brain, compute the gradient vector at each of the 8 vertices
            cube.CalculateGradients();

            // for every edge, find the exact position of the cut vertex if it cuts the brain
            var edges = EdgeTable[cube.Index];
            var cutVertices = new Vector3[12];
            var cutNormals = new Vector3[12];
            
            for (var i = 0; i < 12; i++) {
                if ((edges & (1 << i)) > 0) {
                    var p1 = EndPointsTable[i][0];
                    var p2 = EndPointsTable[i][1];
                    var (position, gradient) = LerpVertex(isoValue, cube.Vertices[p1], cube.Vertices[p2]);
                    cutVertices[i] = position;
                    cutNormals[i] = gradient;
                }
            }
            
            // a cut can create a maximum of 5 triangles (15 vertices) in the cube
            // query the triangle table to find out which edges are actually cut
            var indices = TriangleTable[cube.Index];
            
            for (var i = 0; indices[i] != -1; i += 3) {
                // find the three edges of the triangle
                var e1 = indices[i + 0];
                var e2 = indices[i + 1];
                var e3 = indices[i + 2];
                
                // update the cube's cut vertices (in the same order as they form triangles)
                var v1 = cutVertices[e1];
                var v2 = cutVertices[e2];
                var v3 = cutVertices[e3];
                
                cube.CutVertices.Add(v1);
                cube.CutVertices.Add(v2);
                cube.CutVertices.Add(v3);
                
                // update the normal at each vertex (interpolated gradient using central differences)
                // some gradient vectors near the border may be in the opposite direction, we need to
                // invert them to make sure that they point in the direction of the face normal
                var n1 = cutNormals[e1];
                var n2 = cutNormals[e2];
                var n3 = cutNormals[e3];
                
                var faceNormal = Vector3.Cross(v1 - v2, v1 - v3);

                cube.VertexNormals.Add(n1 * Mathf.Sign(Vector3.Dot(n1, faceNormal)));
                cube.VertexNormals.Add(n2 * Mathf.Sign(Vector3.Dot(n2, faceNormal)));
                cube.VertexNormals.Add(n3 * Mathf.Sign(Vector3.Dot(n3, faceNormal)));
            }
        }

        /// <summary>
        /// Linearly interpolate the position and gradient of a cut vertex based on the two endpoints.
        /// </summary>
        private static (Vector3, Vector3) LerpVertex(float isoValue, CubeVertex v1, CubeVertex v2) {
            if (Mathf.Approximately(Mathf.Abs(isoValue - v1.Value), 1e-5f)) {
                return (v1.Position, v1.Gradient);
            }
            
            if (Mathf.Approximately(Mathf.Abs(isoValue - v2.Value), 1e-5f)) {
                return (v2.Position, v2.Gradient);
            }
            
            if (Mathf.Approximately(Mathf.Abs(v1.Value - v2.Value), 1e-5f)) {
                return (v1.Position, v1.Gradient);
            }

            var weight = (isoValue - v1.Value) / (v2.Value - v1.Value);

            return (Vector3.Lerp(v1.Position, v2.Position, weight),
                Vector3.Lerp(v1.Gradient, v2.Gradient, weight));
        }
        
